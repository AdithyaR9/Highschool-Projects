

public class FishPenguin {

    private int colPos;
    private int rowPos;

    public FishPenguin(int row, int col) {
        rowPos = row;
        colPos = col;
    }

    public int getColPos() {
        return colPos;
    }

    public int getRowPos() {
        return rowPos;
    }

    public void setColPos(int a) {
        colPos = a;
    }

    public void setRowPos(int a) {
        rowPos = a;
    }

}
